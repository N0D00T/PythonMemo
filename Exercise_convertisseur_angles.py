#Exercise, convertisseur d'angles:

# Convertir un degré en radian.
# Pour 1 deg = pi divisé par 180. 
pi = 3.14
# Le nombre pi sera toujours égal à 3.14...
formule_deg_to_rad = pi/180 
angle_deg = 90
result_deg_to_rad = formule_deg_to_rad * angle_deg
print(result_deg_to_rad)


#Convertir un radian en degré:
# Pour 1 rad = pi divisé par 180. 
formule_rad_to_deg = 180/pi
angle_rad = 1.57
result_rad_to_deg = formule_rad_to_deg * angle_rad
print(result_rad_to_deg)
